package  adm.alumno.spring;

public class AdmAlumPre{	
	private String alumFolio;
	private String carreraId;
	private String folio;
	private String estado;
		
	public AdmAlumPre(){
		alumFolio 	= "";
		carreraId 	= "";
		folio 		= ""; 
		estado 		= ""; 
		
	}
	
	/**
	 * @return the codigoPersonal
	 */
	public String getCodigoPersonal() {
		return alumFolio;
	}
	
	/**
	 * @param codigoPersonal the codigoPersonal to set
	 */
	public void setCodigoPersonal(String codigoPersonal) {
		this.alumFolio = codigoPersonal;
	}

	/**
	 * @return the carreraId
	 */
	public String getCarreraId() {
		return carreraId;
	}

	/**
	 * @param carreraId the carreraId to set
	 */
	public void setCarreraId(String carreraId) {
		this.carreraId = carreraId;
	}

	/**
	 * @return the folio
	 */
	public String getFolio() {
		return folio;
	}

	/**
	 * @param folio the folio to set
	 */
	public void setFolio(String folio) {
		this.folio = folio;
	}

	/**
	 * @return the estado
	 */
	public String getEstado() {
		return estado;
	}

	/**
	 * @param estado the estado to set
	 */
	public void setEstado(String estado) {
		this.estado = estado;
	}
	
	public String getAlumFolio() {
		return alumFolio;
	}

	public void setAlumFolio(String alumFolio) {
		this.alumFolio = alumFolio;
	}
	
}		