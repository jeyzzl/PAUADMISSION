package adm.alumno.spring;

public class AdmSocioeco {
	private String codigo;
	private String banos;
	private String autos;
	private String internet;
	private String personas;
	private String cuartos;
	private String ingreso;
	private String capturado;
		
	public AdmSocioeco(){
		codigo 		= "0";
		banos 		= "0";
		autos 		= "0";
		internet	= "S";
		personas 	= "0";
		cuartos 	= "0";
		ingreso		= "0";
		capturado 	= "A";
	}

	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}

	public String getBanos() {
		return banos;
	}

	public void setBanos(String banos) {
		this.banos = banos;
	}

	public String getAutos() {
		return autos;
	}

	public void setAutos(String autos) {
		this.autos = autos;
	}

	public String getInternet() {
		return internet;
	}

	public void setInternet(String internet) {
		this.internet = internet;
	}

	public String getPersonas() {
		return personas;
	}

	public void setPersonas(String personas) {
		this.personas = personas;
	}

	public String getCuartos() {
		return cuartos;
	}

	public void setCuartos(String cuartos) {
		this.cuartos = cuartos;
	}

	public String getIngreso() {
		return ingreso;
	}

	public void setIngreso(String ingreso) {
		this.ingreso = ingreso;
	}

	public String getCapturado() {
		return capturado;
	}

	public void setCapturado(String capturado) {
		this.capturado = capturado;
	}

}